package org.example

/**
 * This enum represents the possible states of a task.
 */
enum class TaskState {
    /**
     * Represents a task that is currently in progress.
     */
    IN_PROGRESS,

    /**
     * Represents a task that is pending.
     */
    PENDING,

    /**
     * Represents a task that has been completed.
     */
    COMPLETED
}